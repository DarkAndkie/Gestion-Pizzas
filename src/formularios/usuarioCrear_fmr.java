    private void user_cmbItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_user_cmbItemStateChanged
        if(!user_cmb.getSelectedItem().equals("Cliente") || !user_cmb.getSelectedItem().equals("Proveedor")){
            passw_txt.setVisible(false);
            passw_Jlbel.setVisible(false);
        }
        if(user_cmb.getSelectedItem().equals("Administrador") || user_cmb.getSelectedItem().equals("Trabajador")){
           {
        passw_txt.setVisible(true);
        passw_Jlbel.setVisible(true);
            }  
        }
    }//GEN-LAST:event_user_cmbItemStateChanged

