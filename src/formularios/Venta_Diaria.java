    private void btn_facturarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_facturarActionPerformed
        if(tbl_venta.getRowCount()!=0 && !txt_idCliente.getText().equals("") ) {
       ventas venta = new ventas();
      venta.cobrar(listaDetalleVenta, txt_Total,idUsuario, txt_idCliente, label_fecha, cmb_iva,idcuadre);
      venta.limpiarTodo(listaDetalleVenta,txt_idItem, txt_nombreitem, txt_tipo, txt_cant, txt_valor,txt_ClienteBusqueda,txt_idCliente,txt_nombreCliente
      ,txt_Iva,txt_Total,tbl_venta,tbl_busqueda,tbl_Cliente);
        }else{
            JOptionPane.showMessageDialog(null, "Debe tener items y un cliente para poder registrar la venta");
        }
    }//GEN-LAST:event_btn_facturarActionPerformed

