    private void btn_tipoAccionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_tipoAccionActionPerformed
        cargos cr = new cargos();
        int op;
        if(btn_tipoAccion.getText().equals("Cargos")){
            op=JOptionPane.showConfirmDialog(null, "Pasará a descargos. Recuerde que toda la información seleccionada se eliminará");
            switch (op) {
                case JOptionPane.YES_OPTION:
            verificarTipo=false;
            btn_tipoAccion.setText("Descargos");
            scroll_desc.setVisible(true);
            lbl_descripcionDes.setVisible(true);
            txt_descripcionDes.setVisible(true);
                    break;
                default:
                    throw new AssertionError();
            }
        }else if(btn_tipoAccion.getText().equals("Descargos")){
            op=JOptionPane.showConfirmDialog(null, "Pasará a descargos. Recuerde que toda la información seleccionada se eliminará");
            switch (op) {
                case JOptionPane.YES_OPTION:
            verificarTipo=true;
            scroll_desc.setVisible(false);
            lbl_descripcionDes.setVisible(false);
            txt_descripcionDes.setVisible(false);
            btn_tipoAccion.setText("Cargos");
                    break;
                default:
                    throw new AssertionError();
            }            

        }
        cr.limpiarTodo(tabla_bucarPr, tablaItem, tablaResumen, Detalle_Lista, txt_proovB, txt_item,txt_idProv,txt_nombreProv,txt_descripcionDes,
               lbl_total);
    }//GEN-LAST:event_btn_tipoAccionActionPerformed

