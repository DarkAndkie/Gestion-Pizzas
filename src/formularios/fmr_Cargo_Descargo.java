    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
      cargos cargosObj = new cargos();
      boolean verificado=false;
        if(!Detalle_Lista.isEmpty() && !txt_idProv.getText().equals("")){
          verificado=true;
      }else{
            JOptionPane.showMessageDialog(null, "Recuerde rellenar toda la información");
        }
        if(verificarTipo==false){
        if(verificado==true && cargosObj.validarRestodeCantidades(Detalle_Lista)==true){
          cargosObj.finalizarMovimiento(Detalle_Lista, txt_idProv, lbl_Fecha, txt_descripcionDes, lbl_total, verificarTipo);
          cargosObj.limpiarTodo(tabla_bucarPr, tablaItem, tablaResumen, Detalle_Lista, txt_proovB, txt_item,txt_idProv,txt_nombreProv,txt_descripcionDes,
               lbl_total);
        } 
        }
          if(verificado==true && verificarTipo==true){
          cargosObj.finalizarMovimiento(Detalle_Lista, txt_idProv, lbl_Fecha, txt_descripcionDes, lbl_total, verificarTipo);
         cargosObj.limpiarTodo(tabla_bucarPr, tablaItem, tablaResumen, Detalle_Lista, txt_proovB, txt_item,txt_idProv,txt_nombreProv,txt_descripcionDes,
               lbl_total);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

